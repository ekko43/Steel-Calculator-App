def pick(prompt, choices):
    """Select from a numbered list by entering the item number."""
    print()
    for i, c in enumerate(choices, 1):
        print(f"  {i:>2}. {c}")
    while True:
        try:
            idx = int(input(f"\n  {prompt}: ")) - 1
            if 0 <= idx < len(choices):
                return choices[idx]
            print("  Out of range — try again.")
        except ValueError:
            print("  Enter a number.")

def pick_section(prompt, choices, default=None):
    """Select a section by typing its name (case-insensitive, partial match allowed).
    Does NOT print the full list — type the name or a partial to search.
    If default is given, pressing Enter reuses it."""
    print()
    choices_upper = [c.upper() for c in choices]
    default_hint = f"  [Enter to reuse: {default}]" if default else ""
    while True:
        raw = input(f"  {prompt}{default_hint}: ").strip()
        if raw == "" and default:
            print(f"  -> Reusing: {default}")
            return default
        if not raw:
            print("  Please type a section name (e.g. W250X58). Partial names are also accepted.")
            continue
        raw_upper = raw.upper()
        # 1. Exact match (case-insensitive)
        if raw_upper in choices_upper:
            return choices[choices_upper.index(raw_upper)]
        # 2. Partial match — must be unique
        matches = [c for c in choices if raw_upper in c.upper()]
        if len(matches) == 1:
            print(f"  -> Matched: {matches[0]}")
            return matches[0]
        elif len(matches) > 1:
            print(f"  Ambiguous — {len(matches)} matches. Showing first 10:")
            for m in matches[:10]:
                print(f"    {m}")
            if len(matches) > 10:
                print(f"    ... and {len(matches)-10} more. Please be more specific.")
        else:
            print(f"  '{raw}' not found. Try a partial name to search (e.g. 'W250' shows all W250 sections).")

def ask_float(prompt, default=None):
    while True:
        raw = input(f"  {prompt}: ").strip()
        if raw == "" and default is not None:
            return default
        try:
            return float(raw)
        except ValueError:
            print("  Enter a number.")

def ask_int(prompt, default=None):
    while True:
        raw = input(f"  {prompt}: ").strip()
        if raw == "" and default is not None:
            return default
        try:
            return int(raw)
        except ValueError:
            print("  Enter an integer.")

def ask_float_with_prev(prompt, prev=None, fallback=None):
    """ask_float that shows and reuses a previous value on Enter."""
    if prev is not None:
        hint = f"  [Enter to reuse: {prev}]"
    elif fallback is not None:
        hint = f"  [default: {fallback}]"
    else:
        hint = ""
    while True:
        raw = input(f"  {prompt}{hint}: ").strip()
        if raw == "":
            if prev is not None:
                return prev
            if fallback is not None:
                return fallback
        try:
            return float(raw)
        except ValueError:
            print("  Enter a number.")

def common_inputs(prev=None):
    """Ask for Lc, K, Pu_kN, Pa_kN. prev=(Lc,K,Pu_kN,Pa_kN) from last run."""
    pLc = prev[0] if prev else None
    pK  = prev[1] if prev else None
    pPu = prev[2] if prev else None
    pPa = prev[3] if prev else None
    print()
    Lc = ask_float_with_prev("Unbraced length Lc (mm)", prev=pLc)
    K  = ask_float_with_prev("Effective length factor K", prev=pK, fallback=1.0)
    print()
    print("  Applied loads — enter 0 to show capacity only.")
    Pu_kN = ask_float_with_prev("  LRFD factored load Pu (kN)  [0 = skip]", prev=pPu, fallback=0.0)
    Pa_kN = ask_float_with_prev("  ASD  service load  Pa (kN)  [0 = skip]", prev=pPa, fallback=0.0)
    return Lc, K, Pu_kN * 1000.0, Pa_kN * 1000.0, Pu_kN, Pa_kN



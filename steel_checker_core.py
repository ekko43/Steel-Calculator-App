def _to_float(v):
    """Convert a database string value to float; returns None for '–' (not applicable)."""
    s = str(v).strip()
    if s in ('–', '-', '—', ''):
        return None
    return float(s)

def run_standard(shape_key, Lc, K, Pu, Pa, prev_section=None):
    db    = SECTIONS[shape_key]
    names = list(db.keys())
    name  = pick_section("Type section name (e.g. W250X58)", names, default=prev_section)
    props = db[name]

    if shape_key == "HSS_RND":
        A   = _to_float(props[0])
        r   = _to_float(props[1])
        rx  = ry = r
    elif shape_key == "L":
        A   = _to_float(props[0])
        rz  = _to_float(props[1])
        rx  = _to_float(props[2])
        # minimum r (rz) governs for single angle
        ry  = rz
    elif shape_key == "2L":
        A    = _to_float(props[0])
        rx   = _to_float(props[1])
        ry_0 = _to_float(props[2])
        ry_3 = _to_float(props[3])   # may be None for LLBB/SLBB unequal-leg entries
        ry_9 = _to_float(props[4])   # may be None for LLBB/SLBB unequal-leg entries

        if ry_3 is None and ry_9 is None:
            # LLBB / SLBB sections — only 0-separation ry is tabulated.
            # Only allow separation = 0 for these entries.
            print("  Note: this section only has tabulated ry for 0 mm separation.")
            s = 0
        else:
            s = ask_int("  Back-to-back separation (mm)  [0 / 3 / 9]  [0]", 0)

        if s <= 0 or ry_3 is None:
            ry = ry_0
        elif s <= 6 or ry_9 is None:
            ry = ry_3
        else:
            ry = ry_9
    else:
        A   = _to_float(props[0])
        rx  = _to_float(props[1])
        ry  = _to_float(props[2])

    label = f"{shape_key}  {name}"
    compression_report(A, rx, ry, Lc, K, Pu, Pa, label)
    return name



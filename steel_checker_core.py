def critical_stress(KL_r):
    """Return Fcr (Pa) given slenderness ratio KL/r."""
    Fe    = math.pi**2 * E / KL_r**2
    limit = 4.71 * math.sqrt(E / Fy)
    if KL_r <= limit:
        return (0.658 ** (Fy / Fe)) * Fy   # inelastic buckling
    return 0.877 * Fe                       # elastic buckling


def compression_report(A_mm2, rx_mm, ry_mm, Lc_mm, K, Pu_N, Pa_N, label):
    """
    Full compression adequacy report.
    A_mm2   : gross area in mm2
    rx, ry  : radii of gyration in mm
    Lc_mm   : unbraced length in mm
    K       : effective-length factor
    Pu_N    : LRFD factored axial load in N  (0 = skip)
    Pa_N    : ASD service axial load in N    (0 = skip)
    """
    KLx = K * Lc_mm / rx_mm
    KLy = K * Lc_mm / ry_mm
    KLr = max(KLx, KLy)

    Fcr_val = critical_stress(KLr)
    Fe_val  = math.pi**2 * E / KLr**2
    Pn      = Fcr_val * (A_mm2 * 1e-6)   # Pa * m2 = N

    phi_Pn = PHI_C   * Pn
    Pn_asd = Pn / OMEGA_C

    header(f"COMPRESSION CHECK  (AISC 360-22 Sec. E3)\n  {label}")
    sep()
    fmt("Gross area, A",              A_mm2,  "mm2")
    fmt("Radius of gyration, rx",     rx_mm,  "mm")
    fmt("Radius of gyration, ry",     ry_mm,  "mm")
    fmt("Unbraced length, Lc",        Lc_mm,  "mm")
    fmt("Effective length factor, K", K,      "")
    sep()
    fmt("KL/rx",                      KLx,             "")
    fmt("KL/ry",                      KLy,             "")
    fmt("Governing KL/r",             KLr,             "")
    fmt("Elastic buckling stress, Fe", Fe_val / 1e6,   "MPa")
    fmt("Critical stress, Fcr",       Fcr_val / 1e6,   "MPa")
    fmt("Nominal strength, Pn",       kN(Pn),          "kN")
    sep()

    # ── LRFD ──
    print("  [LRFD]")
    fmt("  phi_c * Pn  (design strength)", kN(phi_Pn), "kN")
    if Pu_N > 0:
        dcr = Pu_N / phi_Pn
        fmt("  Applied factored load, Pu", kN(Pu_N), "kN")
        fmt("  DCR = Pu / (phi_c*Pn)",    dcr,       "")
        tag = "ADEQUATE" if dcr <= 1.0 else "INADEQUATE"
        print(f"\n  LRFD Result: {'OK  ' if dcr<=1.0 else 'FAIL'}  {tag}\n")
    else:
        print("  (No LRFD demand — capacity shown only)\n")

    # ── ASD ──
    print("  [ASD]")
    fmt("  Pn / Omega_c  (allowable strength)", kN(Pn_asd), "kN")
    if Pa_N > 0:
        dcr = Pa_N / Pn_asd
        fmt("  Applied service load, Pa", kN(Pa_N), "kN")
        fmt("  DCR = Pa / (Pn/Omega_c)", dcr,       "")
        tag = "ADEQUATE" if dcr <= 1.0 else "INADEQUATE"
        print(f"\n  ASD Result: {'OK  ' if dcr<=1.0 else 'FAIL'}  {tag}\n")
    else:
        print("  (No ASD demand — capacity shown only)\n")
    sep("=")



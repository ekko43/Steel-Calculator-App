def builtup_channel_W(w_name, c_name, gap_mm=0.0):
    """
    Built-up: one Channel (C) seated on top of a Wide-Flange (W).
    The channel's web points upward; both sections share the same
    vertical axis of symmetry (y-axis).

    gap_mm : clear gap between top face of W top-flange and
             bottom of C web (default 0).

    Returns (A_total mm2, rx mm, ry mm, description).
    """
    # W props: (A, rx, ry, bf, tf, d, tw)  — stored as strings in the DB
    wA, wrx, wry, wbf, wtf, wd, wtw = [float(v) for v in W_DB[w_name]]

    # C props: (A, rx, ry, d, bf, tf, tw)  — stored as strings in the DB
    cA, crx, cry, cd, cbf, ctf, ctw = [float(v) for v in C_DB[c_name]]

    # Use tabulated Ix = A*rx^2, Iy = A*ry^2 for each component
    Ix_w = wA * wrx**2
    Iy_w = wA * wry**2
    Ix_c = cA * crx**2
    Iy_c = cA * cry**2

    # Centroid locations measured from W centroid (mid-depth of W)
    yd_w = 0.0
    yd_c = wd / 2.0 + gap_mm + cd / 2.0

    A_tot = wA + cA
    y_bar = (wA * yd_w + cA * yd_c) / A_tot   # combined centroid

    # Parallel-axis theorem for Ix
    Ix = (Ix_w + wA * (yd_w - y_bar)**2 +
          Ix_c + cA * (yd_c - y_bar)**2)

    # Iy: both centred on same vertical axis
    Iy = Iy_w + Iy_c

    rx = math.sqrt(Ix / A_tot)
    ry = math.sqrt(Iy / A_tot)

    desc = (f"Built-up C+W  [{c_name}  on  {w_name},  gap={gap_mm:.0f} mm]")
    return A_tot, rx, ry, desc


def builtup_plate_W(bf_top, tf_top, hw, tw, bf_bot=None, tf_bot=None):
    """
    Plate-built-up welded I-section (W-shape from plates).
    All dimensions in mm.

    bf_top, tf_top : top flange width & thickness
    hw, tw         : web clear height & thickness
    bf_bot, tf_bot : bottom flange (None -> same as top -> doubly symmetric)

    Returns (A_total mm2, rx mm, ry mm, description).
    """
    if bf_bot is None:
        bf_bot, tf_bot = bf_top, tf_top

    A_top = bf_top * tf_top
    A_web = hw     * tw
    A_bot = bf_bot * tf_bot
    A_tot = A_top + A_web + A_bot

    # Centroid of each part from bottom of section
    y_bot = tf_bot / 2.0
    y_web = tf_bot + hw / 2.0
    y_top = tf_bot + hw + tf_top / 2.0

    y_bar = (A_bot * y_bot + A_web * y_web + A_top * y_top) / A_tot

    # Ix by parallel-axis theorem  (own centroidal + transfer)
    def rect_Ix_own(b, h):   return b * h**3 / 12.0
    def rect_Iy_own(b, h):   return h * b**3 / 12.0

    Ix = (rect_Ix_own(bf_top, tf_top) + A_top * (y_top - y_bar)**2 +
          rect_Ix_own(tw,     hw)     + A_web * (y_web - y_bar)**2 +
          rect_Ix_own(bf_bot, tf_bot) + A_bot * (y_bot - y_bar)**2)

    Iy = (rect_Iy_own(bf_top, tf_top) +
          rect_Iy_own(tw,     hw)     +
          rect_Iy_own(bf_bot, tf_bot))

    rx = math.sqrt(Ix / A_tot)
    ry = math.sqrt(Iy / A_tot)

    sym = ("Double-symmetric"
           if (bf_top == bf_bot and tf_top == tf_bot) else "Single-symmetric")
    desc = (f"Built-up Plate W  [{sym}]  "
            f"Top={bf_top}x{tf_top}  Web={hw}x{tw}  Bot={bf_bot}x{tf_bot} mm")
    return A_tot, rx, ry, desc


